/**
 * The predefined type of custom skybox.
 * @enum {Number}
 *
 */
var SkyboxType = {
  /**
   * The sunny skybox.
   *
   * @type {Number}
   * @constant
   */
  SUNNY: 1,

  /**
   * The dusk skybox.
   *
   * @type {Number}
   * @constant
   */
  DUSK: 2
};
export default Object.freeze(SkyboxType);
